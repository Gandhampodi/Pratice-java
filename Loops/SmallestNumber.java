package Loops;


public class SmallestNumber {
    public static void main(String[] args) {
        int a=9;
        int b=6;
        int c=3;
        if(a<b){
            System.out.println("A is less than b");
        }
        else if(b<c){
            System.out.println("B is less than c");
        }
        else if(c<a){
            System.out.println("C is less than a");
        }
    }
}
